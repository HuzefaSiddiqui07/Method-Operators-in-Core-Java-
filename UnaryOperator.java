// Unary Operator

public class UnaryOperator {

	public static void main(String[] args) {
		
		int j = 48;
		
		// Increment Operator (++)
		
		j++;
		System.out.println("Value of j is :" + j);
		
		// Decrement Operator (--)
		
		j--;
		System.out.println("Value of j is :" + j);
		

	}

}

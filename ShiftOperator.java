// Shift Operator

public class ShiftOperator {

	public static void main(String[] args) {
		
		int s = 20;
		
		// Right Shift Operator (>>)
		
		System.out.println(s>>2);
		System.out.println(s>>3);
		
		// Left Shift Operator (<<)
		
		System.out.println(s<<2);
		System.out.println(s<<3);
		

	}

}

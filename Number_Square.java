// METHOD RETURN TYPE

public class Number_Square {
	// Create a method
	public static int square(int num) {
		// return statement
		return num * num; 
	}

	public static void main(String[] args) {
		int result;
		// call the method 
		// store returned value to result 
		result = square(20);
		
		System.out.println("Sqaured Value Of 20 Is :" + result);
		
	}

}

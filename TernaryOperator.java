// Ternary Operator

public class TernaryOperator {

	public static void main(String[] args) {
		
		int u = 56;
		int v = 36;
		
		int num1 = (u>v)?u:v; // Condition is true & print 'u' value print 
		
		int num2 = (u<v)?u:v; // Condition is false & print 'v' value print 
		
		System.out.println("Value of num1 :" + num1);
		
		System.out.println("Value of num2 :" + num2);
	}

}

// Arithmetic Operator
public class ArithmeticOperators {

	public static void main(String[] args) {
		int h = 48;
		int t = 35;
		// Mathematical Operations 
		System.out.println("Addition :" + (h + t));
		System.out.println("Subtraction :" + (h - t));
		System.out.println("Multipication :" + (h * t));
		System.out.println("Division :" + (h /  t));
		System.out.println("Modules Or Reminder :" + (h % t));
			}

}
 


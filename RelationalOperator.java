// Relational Operator

public class RelationalOperator {

	public static void main(String[] args) {
		
		int j = 85;
		int k = 75;
		
		// Operation Like; ( >,<,>=,<=,==,!= )
		
		System.out.println(j>k); // Greater than
		System.out.println(j<k); // Less than
		System.out.println(j>=k); // Greater than equal to
		System.out.println(j<=k); // Less than equal to
		System.out.println(j==k); // Equal to
		System.out.println(j!=k); // Not equal to

	}

}

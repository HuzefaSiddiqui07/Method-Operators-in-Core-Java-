// Assignment Operator

public class AssignmentOperator {

	public static void main(String[] args) {
		
		int m = 32;
		int n = 27;
		
		m = m+n; // Add
		m = m-n; // Subtract
		
		// Print value of m & n 
		
		System.out.println("Value of m = "+ m);
		System.out.println("Value of m = "+ m);

	}

}
